#É uma boa prática seguir algumas convenções para nomear variáveis, como o Snake Case ou Underscore case
#1. nomes semanticos
#2. podem conter letras e números
#3. começa com letras minúsculas e utiliza o underscore (_) para separar palavras
#4. não pode iniciar com número
#5. não pode conter caractere especial
#6. não pode ser palavras reservadas, como: class, and, false, for...

