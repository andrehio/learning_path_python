#Operadores aritméticos
import math #Para certas operações é necessário usar esta biblioteca

#Adição
print('Soma de 1+1 = {}'.format(1+1),"\n")

#Subtração
print('Diferença de 48-23 = {}'.format(48-23),"\n")

#Multiplicação
print('Produto de 56*7 = {}'.format(56*7),"\n")

#Divisão
print('Divisão de 89/7 = {}'.format(89/7),"\n")

#Divisão inteira
print('Divisão inteira de 8//3 = {}'.format(8//3),"\n")

#Módulo
print('Resto da divisão de 8%3 = {}'.format(8%3),"\n")

#Exponenciação
print('Exponencial de 5**2 = {}'.format(5**2),"\n")

#Raiz (necessário biblioteca math)
print('Raiz quadrada de 49 = {}'.format(math.sqrt(49)),"\n")

#Logarítmo(necessário biblioteca math - math.log(x[, base]))
print('Log de 10 = {}'.format(math.log(10)),"\n")

